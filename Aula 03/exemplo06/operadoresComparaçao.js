//Maior que >
console.log("2 > 1 = "+ (2>1))

//Menor que <
console.log("2 < 1 = "+ (2<1))

//Maior ou igual >=
console.log("2 >= 2 = "+ (2>=2))

//Menor ou igual <=
console.log("2 <= 2 = "+ (2<=2))

//igual (compara) ==
console.log("2 == 2 = "+ (2==2))

//Diferente !=
console.log("2 != 2 = "+ (2!=3))

//Identico ===
console.log("2 === 2 = "+ (2==="2"))
